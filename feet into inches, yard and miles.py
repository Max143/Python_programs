# convert feet into inche, yards, miles

"""
Formul-
inch = feet * 12
yards = feet / 3.0
miles = feet / 5280

"""


feet = int(input("Input the feet : "))

inches = feet * 12

yards = feet / 3.0

miles = feet / 5280

print("The distance in inches is %d. " % inches)

print("The distance in yards is  %d. " % yards)

print("The distance is miles is %d. " % miles)

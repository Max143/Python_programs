# write python to reverse a given number

# using loop
# using recursion
# using function

# 1. By using loop method

number = int(input("Enter number: "))

Reverse = 0
while (number > 0):
    Remainder = number % 10
    Reverse = (Reverse*10) + Remainder
    number = number // 10

print("In reverse of entered no. is %d ", Reverse)

